package PGO3.Lecture;
public class Employee {
    public String Fname;
    public String Lname;
    public int Phone;
    public int Pesel;
    public String Class;

    public Employee(String Fname, String Lname, int Phone, int Pesel, String Class) {
        this.Fname = Fname;
        this.Lname = Lname;
        this.Phone = Phone;
        this.Pesel = Pesel;
        this.Class = Class;
    }
}